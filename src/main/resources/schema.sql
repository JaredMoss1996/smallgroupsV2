CREATE TABLE churches
(
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name         VARCHAR(255) NOT NULL,
    address      VARCHAR(512),
    contact_info VARCHAR(255)
);

CREATE TABLE roles
(
    id   SMALLINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE app_user
(
    id        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username  VARCHAR(255) UNIQUE NOT NULL,
    password  VARCHAR(255)        NOT NULL,
    enabled   BOOLEAN             NOT NULL,
    role_id   SMALLINT            NOT NULL,
    church_id BIGINT,
    FOREIGN KEY (role_id) REFERENCES roles (id),
    FOREIGN KEY (church_id) REFERENCES churches (id) ON DELETE SET NULL
);


CREATE TABLE permissions
(
    id   SMALLINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(40) NOT NULL UNIQUE
);

CREATE TABLE role_permissions
(
    role_id       SMALLINT NOT NULL,
    permission_id SMALLINT NOT NULL,
    PRIMARY KEY (role_id, permission_id),
    FOREIGN KEY (role_id) REFERENCES roles (id) ON DELETE CASCADE,
    FOREIGN KEY (permission_id) REFERENCES permissions (id) ON DELETE CASCADE
);

CREATE TABLE ages
(
    id   SMALLINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(15) NOT NULL UNIQUE
);

CREATE TABLE genders
(
    id   SMALLINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(15) NOT NULL UNIQUE
);

CREATE TABLE groups
(
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title        VARCHAR(255) NOT NULL,
    description  TEXT,
    schedule     VARCHAR(255),
    location     VARCHAR(255),
    address      VARCHAR(512),
    contact_info VARCHAR(255),
    gender_id       SMALLINT,
    age_id          SMALLINT,
    frequency    VARCHAR(50),
    church_id    BIGINT,
    FOREIGN KEY (church_id) REFERENCES churches (id) ON DELETE CASCADE,
    FOREIGN KEY (gender_id) REFERENCES genders (id),
    FOREIGN KEY (age_id) REFERENCES ages (id)
);

CREATE TABLE group_leaders
(
    group_id  BIGINT NOT NULL,
    member_id BIGINT NOT NULL,
    PRIMARY KEY (group_id, member_id),
    FOREIGN KEY (member_id) REFERENCES app_user (id) ON DELETE CASCADE,
    FOREIGN KEY (group_id) REFERENCES groups (id) ON DELETE CASCADE
);
CREATE INDEX idx_group_leaders_member ON group_leaders (member_id);

CREATE TABLE group_members
(
    group_id  BIGINT NOT NULL,
    member_id BIGINT NOT NULL,
    PRIMARY KEY (group_id, member_id),
    FOREIGN KEY (member_id) REFERENCES app_user (id) ON DELETE CASCADE,
    FOREIGN KEY (group_id) REFERENCES groups (id) ON DELETE CASCADE
);
CREATE INDEX idx_group_members_member ON group_members (member_id);

CREATE TABLE category
(
    id   SMALLINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL UNIQUE
);

CREATE TABLE group_categories
(
    group_id    BIGINT NOT NULL,
    category_id SMALLINT NOT NULL,
    PRIMARY KEY (group_id, category_id),
    FOREIGN KEY (group_id) REFERENCES groups (id),
    FOREIGN KEY (category_id) REFERENCES category (id)
);
CREATE INDEX idx_group_categories_category ON group_categories (category_id);
